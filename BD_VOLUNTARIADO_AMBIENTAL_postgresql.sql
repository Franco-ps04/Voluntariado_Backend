-- ============================================================
-- 11. CERTIFICADO
-- ============================================================
CREATE TABLE Certificado (
  id_certificado        INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  titulo                VARCHAR(100) NOT NULL,
  motivo                VARCHAR(200) NULL,
  color                 VARCHAR(20)  NOT NULL DEFAULT 'success',
  cantidad_participacion INTEGER NOT NULL, -- cuántos eventos al momento de emitir
  fecha_emision         DATE NOT NULL DEFAULT CURRENT_DATE,
  archivo_url           VARCHAR(500) NULL,
  id_voluntario         INTEGER NOT NULL REFERENCES Voluntario(id_usuario),
  id_config             INTEGER NOT NULL REFERENCES ConfigCertificado(id_config)
);

-- ============================================================
-- 12. MENSAJE
-- Voluntario envía mensaje y puede ir a un Usuario destino
-- (Administrador u Organizador)
-- ============================================================
CREATE TABLE Mensaje (
  id_mensaje            INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  asunto                VARCHAR(150) NOT NULL,
  mensaje               TEXT NOT NULL,
  fecha                 TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  leido                 BOOLEAN NOT NULL DEFAULT FALSE,
  leido_por_voluntario  BOOLEAN NOT NULL DEFAULT FALSE,
  respondido            BOOLEAN NOT NULL DEFAULT FALSE,
  id_voluntario         INTEGER NOT NULL REFERENCES Voluntario(id_usuario),
  id_usuario_destino    INTEGER NOT NULL REFERENCES Usuario(id_usuario),
  id_evento             INTEGER NULL REFERENCES Evento(id_evento)
);

-- ============================================================
-- 13. NOTIFICACION
-- Emisor: Usuario (admin u organizador)
-- Destino: voluntarios inscritos al evento
-- ============================================================
CREATE TABLE Notificacion (
  id_notificacion INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  titulo          VARCHAR(200) NOT NULL,
  mensaje         TEXT NOT NULL,
  fecha           TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  id_usuario      INTEGER NOT NULL REFERENCES Usuario(id_usuario),
  id_evento       INTEGER NOT NULL REFERENCES Evento(id_evento)
);

-- ============================================================
-- 14. CONTACTO
-- Formulario público, independiente
-- ============================================================
CREATE TABLE Contacto (
  id_contacto INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre      VARCHAR(100) NOT NULL,
  telefono    VARCHAR(9) NOT NULL,
  email       VARCHAR(100) NOT NULL,
  asunto      VARCHAR(150) NOT NULL,
  mensaje     TEXT NOT NULL,
  fecha       TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  leido       BOOLEAN NOT NULL DEFAULT FALSE
);

-- ============================================================
-- 15. RespuestaMensaje
-- ============================================================
CREATE TABLE RespuestaMensaje (
  id_respuesta INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  id_mensaje   INTEGER NOT NULL
               REFERENCES Mensaje(id_mensaje) ON DELETE CASCADE,
  texto        TEXT NOT NULL,
  fecha        TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  id_usuario   INTEGER NOT NULL    -- admin u organizador que responde
               REFERENCES Usuario(id_usuario)
);

-- ============================================================
-- 16. NotificacionLeida
-- ============================================================
CREATE TABLE NotificacionLeida (
  id_notificacion INTEGER NOT NULL REFERENCES Notificacion(id_notificacion) ON DELETE CASCADE,
  id_voluntario   INTEGER NOT NULL REFERENCES Voluntario(id_usuario) ON DELETE CASCADE,
  fecha_lectura   TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT PK_NotificacionLeida PRIMARY KEY (id_notificacion, id_voluntario)
);

