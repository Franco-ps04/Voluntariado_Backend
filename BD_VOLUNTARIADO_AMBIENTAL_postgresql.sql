-- ============================================================
-- TRIGGER 1: Evento.inscritos automático
-- ============================================================
CREATE OR REPLACE FUNCTION fn_tr_inscripcion_count()
RETURNS TRIGGER AS $$
DECLARE
  v_evento_id INTEGER;
BEGIN
  IF TG_OP IN ('INSERT', 'UPDATE') THEN
    v_evento_id := NEW.id_evento;
    UPDATE Evento
    SET inscritos = (
      SELECT COUNT(*)
      FROM Inscripcion
      WHERE id_evento = v_evento_id
        AND estado <> 'Cancelado'
    )
    WHERE id_evento = v_evento_id;
  END IF;

  IF TG_OP IN ('DELETE', 'UPDATE') THEN
    IF TG_OP = 'UPDATE' AND NEW.id_evento IS NOT DISTINCT FROM OLD.id_evento THEN
      NULL;
    ELSE
      v_evento_id := OLD.id_evento;
      UPDATE Evento
      SET inscritos = (
        SELECT COUNT(*)
        FROM Inscripcion
        WHERE id_evento = v_evento_id
          AND estado <> 'Cancelado'
      )
      WHERE id_evento = v_evento_id;
    END IF;
  END IF;

  RETURN NULL;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS TR_Inscripcion_Count ON Inscripcion;
CREATE TRIGGER TR_Inscripcion_Count
AFTER INSERT OR DELETE OR UPDATE ON Inscripcion
FOR EACH ROW
EXECUTE FUNCTION fn_tr_inscripcion_count();

-- ============================================================
-- TRIGGER 2: marcar Mensaje.respondido = TRUE al insertar respuesta,
--            y resetear leido_por_voluntario = FALSE (hay nueva resp.)
-- ============================================================
CREATE OR REPLACE FUNCTION fn_tr_respuesta_mensaje_insert()
RETURNS TRIGGER AS $$
BEGIN
  UPDATE Mensaje
  SET respondido = TRUE,
      leido_por_voluntario = FALSE
  WHERE id_mensaje = NEW.id_mensaje;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS TR_RespuestaMensaje_Insert ON RespuestaMensaje;
CREATE TRIGGER TR_RespuestaMensaje_Insert
AFTER INSERT ON RespuestaMensaje
FOR EACH ROW
EXECUTE FUNCTION fn_tr_respuesta_mensaje_insert();

-- ============================================================
-- TRIGGER 3: Certificado automático por asistencia
-- ============================================================
CREATE OR REPLACE FUNCTION fn_tr_asistencia_certificado()
RETURNS TRIGGER AS $$
DECLARE
  v_total_asistencias INTEGER;
  v_id_voluntario INTEGER;
  rec RECORD;
BEGIN
  IF TG_OP = 'UPDATE' AND NEW.asistio = TRUE AND OLD.asistio = FALSE THEN
    SELECT ins.id_voluntario
      INTO v_id_voluntario
    FROM Inscripcion ins
    WHERE ins.id_inscripcion = NEW.id_inscripcion;

    SELECT COUNT(*)
      INTO v_total_asistencias
    FROM Asistencia a
    JOIN Inscripcion ins ON a.id_inscripcion = ins.id_inscripcion
    WHERE ins.id_voluntario = v_id_voluntario
      AND a.asistio = TRUE;

    FOR rec IN
      SELECT id_config, umbral, titulo, motivo, color
      FROM ConfigCertificado
      WHERE activo = TRUE
        AND umbral <= v_total_asistencias
      ORDER BY umbral
    LOOP
      IF NOT EXISTS (
        SELECT 1
        FROM Certificado c
        WHERE c.id_voluntario = v_id_voluntario
          AND c.id_config = rec.id_config
      ) THEN
        INSERT INTO Certificado
          (titulo, motivo, color, cantidad_participacion,
           fecha_emision, id_voluntario, id_config)
        VALUES
          (rec.titulo, rec.motivo, rec.color, v_total_asistencias,
           CURRENT_DATE, v_id_voluntario, rec.id_config);
      END IF;
    END LOOP;
  END IF;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS TR_Asistencia_Certificado ON Asistencia;
CREATE TRIGGER TR_Asistencia_Certificado
AFTER UPDATE ON Asistencia
FOR EACH ROW
EXECUTE FUNCTION fn_tr_asistencia_certificado();
