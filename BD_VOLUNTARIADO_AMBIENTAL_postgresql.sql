-- ============================================================
-- 2. USUARIO
-- ============================================================
CREATE TABLE Usuario (
  id_usuario INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre     VARCHAR(120) NOT NULL,
  email      VARCHAR(120) NOT NULL UNIQUE,
  contrasena VARCHAR(255) NOT NULL, -- bcrypt hash
  telefono   VARCHAR(9)   NOT NULL,
  rol        VARCHAR(15)  NOT NULL DEFAULT 'voluntario'
             CONSTRAINT CHK_rol CHECK (rol IN ('voluntario', 'admin', 'organizador')),
  activo     BOOLEAN      NOT NULL DEFAULT TRUE,
  creado_en  TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- ============================================================
-- 3. VOLUNTARIO
-- ============================================================
CREATE TABLE Voluntario (
  id_usuario INTEGER PRIMARY KEY
             REFERENCES Usuario(id_usuario) ON DELETE CASCADE
);

-- ============================================================
-- 4. ADMINISTRADOR
-- ============================================================
CREATE TABLE Administrador (
  id_usuario INTEGER PRIMARY KEY
             REFERENCES Usuario(id_usuario) ON DELETE CASCADE
);

-- ============================================================
-- 5. ORGANIZADOR
-- ============================================================
CREATE TABLE Organizador (
  id_organizador      INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre_organizacion VARCHAR(100) NOT NULL,
  id_usuario          INTEGER NOT NULL UNIQUE
                      REFERENCES Usuario(id_usuario) ON DELETE CASCADE
);

-- ============================================================
-- 6. EVENTO
-- ============================================================
CREATE TABLE Evento (
  id_evento      INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre         VARCHAR(200) NOT NULL,
  descripcion    TEXT NOT NULL,
  fecha          DATE NOT NULL,
  hora           TIME NOT NULL,
  ubicacion      VARCHAR(300) NOT NULL,
  capacidad      INTEGER NOT NULL DEFAULT 30,
  inscritos      INTEGER NOT NULL DEFAULT 0, -- actualizado por trigger
  estado         VARCHAR(13) NOT NULL DEFAULT 'Próximo'
                 CONSTRAINT CHK_estado CHECK (estado IN ('Próximo', 'En curso', 'Finalizado', 'Cancelado')),
  archivado BOOLEAN NOT NULL DEFAULT FALSE,
  latitud        NUMERIC(10,7) NULL,  -- para Leaflet: marcador en el mapa
  longitud       NUMERIC(10,7) NULL,  -- para Leaflet: marcador en el mapa
  imagen_url     VARCHAR(500) NULL,   -- ruta /uploads/eventos/archivo.jpg
  id_tipo        INTEGER NOT NULL REFERENCES TipoEvento(id_tipo),
  id_organizador INTEGER NOT NULL REFERENCES Organizador(id_organizador)
);

-- ============================================================
-- 7. EVENTO_REQUISITO
-- ============================================================
CREATE TABLE EventoRequisito (
  id_requisito INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  id_evento    INTEGER NOT NULL REFERENCES Evento(id_evento) ON DELETE CASCADE,
  descripcion  VARCHAR(250) NOT NULL,
  orden        INTEGER NOT NULL DEFAULT 0
);

-- ============================================================
-- 8. INSCRIPCION
-- ============================================================
CREATE TABLE Inscripcion (
  id_inscripcion    INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  fecha_inscripcion DATE NOT NULL DEFAULT CURRENT_DATE,
  estado            VARCHAR(15) NOT NULL DEFAULT 'Próximo'
                    CONSTRAINT CHK_ins CHECK (estado IN ('Próximo', 'Finalizado', 'Cancelado')),
  id_voluntario     INTEGER NOT NULL REFERENCES Voluntario(id_usuario),
  id_evento         INTEGER NOT NULL REFERENCES Evento(id_evento),
  CONSTRAINT UQ_Inscripcion UNIQUE (id_voluntario, id_evento)
);

-- ============================================================
-- 9. ASISTENCIA
-- 1 inscripción = 1 asistencia
-- ============================================================
CREATE TABLE Asistencia (
  id_asistencia  INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  id_inscripcion INTEGER NOT NULL UNIQUE
                 REFERENCES Inscripcion(id_inscripcion) ON DELETE CASCADE,
  asistio        BOOLEAN NOT NULL DEFAULT FALSE,
  fecha_registro TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- ============================================================
-- 10. CONFIGURACIÓN DE CERTIFICADOS
-- ============================================================
CREATE TABLE ConfigCertificado (
  id_config INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  titulo    VARCHAR(100) NOT NULL,
  motivo    VARCHAR(200) NULL,
  color     VARCHAR(20)  NOT NULL DEFAULT 'success',
  umbral    INTEGER      NOT NULL,   -- nro de participaciones para emitir
  activo    BOOLEAN      NOT NULL DEFAULT TRUE
);

INSERT INTO ConfigCertificado (titulo, motivo, color, umbral) VALUES
